def _rtf_escape(s: str) -> str:
    out: list[str] = []
    for c in s:
        if c == "\\":
            out.append("\\\\")
        elif c == "{":
            out.append("\\{")
        elif c == "}":
            out.append("\\}")
        elif c == "\n":
            out.append("\\par\n")
        elif ord(c) < 128:
            out.append(c)
        else:
            cp = ord(c)
            if cp > 32767:
                cp -= 65536
            out.append(f"\\u{cp}?")
    return "".join(out)


def _p(text: str) -> str:
    return _rtf_escape(text.strip()) + "\\par\n"


